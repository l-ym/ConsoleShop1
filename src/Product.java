public class Product//实体类（bean）
{
    String pid;
    String pname;
    String price;
    String pdescr;

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public String getPname() {
        return pname;
    }

    public void setPname(String pname) {
        this.pname = pname;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getPdescr() {
        return pdescr;
    }

    public void setPhone(String pdescr) {
        this.pdescr = pdescr;
    }
}
